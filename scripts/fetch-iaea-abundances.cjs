#!/usr/bin/env node
/**
 * Fetch IAEA isotopic abundance data from LiveChart NDS API
 * and generate a TypeScript constants file for use in the app.
 *
 * IAEA LiveChart REST API docs:
 *   https://www-nds.iaea.org/relnsd/vcharthtml/api_v0_guide.html
 *
 * Usage: node scripts/fetch-iaea-abundances.cjs
 * Output: src/constants/iaeaAbundances.ts
 */

const https = require('https');
const fs = require('fs');
const path = require('path');

const IAEA_API_URL = 'https://nds.iaea.org/relnsd/v1/data?fields=ground_states&nuclides=all';
const OUTPUT_FILE = path.join(__dirname, '..', 'src', 'constants', 'iaeaAbundances.ts');

function httpsGet(url) {
  return new Promise((resolve, reject) => {
    https.get(url, (res) => {
      if (res.statusCode < 200 || res.statusCode >= 300) {
        reject(new Error(`HTTP ${res.statusCode}: ${res.statusMessage} for ${url}`));
        res.resume(); // Drain response to free socket
        return;
      }
      let data = '';
      res.on('data', (chunk) => { data += chunk; });
      res.on('end', () => resolve(data));
      res.on('error', reject);
    }).on('error', reject);
  });
}

function parseCSV(csv) {
  const lines = csv.trim().split('\n');
  if (lines.length < 2) throw new Error('Empty CSV response from IAEA API');

  const headers = lines[0].split(',').map(h => h.trim());

  // Validate expected columns are present
  const requiredColumns = ['z', 'n', 'symbol', 'abundance'];
  for (const col of requiredColumns) {
    if (!headers.includes(col)) {
      throw new Error(`Missing required column '${col}' in CSV. Got: ${headers.join(', ')}`);
    }
  }

  const rows = [];

  for (let i = 1; i < lines.length; i++) {
    const values = lines[i].split(',');
    if (values.length !== headers.length) {
      console.warn(`Skipping row ${i}: expected ${headers.length} columns, got ${values.length}`);
      continue;
    }
    const row = {};
    headers.forEach((h, idx) => {
      row[h] = values[idx]?.trim() ?? '';
    });
    rows.push(row);
  }

  return rows;
}

async function main() {
  console.log('Fetching IAEA ground states data...');
  const csv = await httpsGet(IAEA_API_URL);
  const rows = parseCSV(csv);
  console.log(`Parsed ${rows.length} nuclides from IAEA API`);

  // Extract only nuclides with abundance data
  const abundances = [];
  const elementAbundances = {}; // Group by element for summary

  for (const row of rows) {
    const z = parseInt(row.z, 10);
    const n = parseInt(row.n, 10);
    const a = z + n;
    const symbol = row.symbol;
    const abundance = row.abundance ? parseFloat(row.abundance) : null;
    const uncertainty = row.unc_a ? parseFloat(row.unc_a) : null;

    if (abundance === null || isNaN(abundance) || abundance <= 0) continue;

    // Create Parkhomov-style notation
    // For hydrogen isotopes: map A=2 to D, A=3 to T (matching Parkhomov DB convention)
    let displaySymbol = symbol;
    if (z === 1 && a === 2) displaySymbol = 'D';
    else if (z === 1 && a === 3) displaySymbol = 'T';

    const entry = {
      z,
      a,
      symbol: displaySymbol,
      abundance: Math.round(abundance * 10000) / 10000, // 4 decimal places
      uncertainty: uncertainty !== null ? Math.round(uncertainty * 10000) / 10000 : null,
    };

    abundances.push(entry);

    // Track per-element totals
    if (!elementAbundances[symbol]) {
      elementAbundances[symbol] = { z, isotopes: 0, total: 0 };
    }
    elementAbundances[symbol].isotopes++;
    elementAbundances[symbol].total += abundance;
  }

  console.log(`Found ${abundances.length} nuclides with abundance data across ${Object.keys(elementAbundances).length} elements`);

  // Sort by Z then A
  abundances.sort((a, b) => a.z - b.z || a.a - b.a);

  // Generate TypeScript file
  const extractionDate = new Date().toISOString().split('T')[0];

  const tsContent = `/**
 * IAEA isotopic abundance data
 *
 * Source: IAEA Nuclear Data Services - LiveChart API
 * URL: https://nds.iaea.org/relnsd/v1/data?fields=ground_states&nuclides=all
 * Data vintage: NuBase 2020 / NUBASE2020 evaluation
 * Extracted: ${extractionDate}
 *
 * Values are natural isotopic abundance percentages (mole fraction * 100).
 * Uncertainties are in the same units.
 *
 * This file is auto-generated by scripts/fetch-iaea-abundances.cjs
 * Do not edit manually.
 */

export interface IAEAAbundance {
  /** Atomic number */
  z: number
  /** Mass number */
  a: number
  /** Element symbol (uses D/T for deuterium/tritium to match Parkhomov convention) */
  symbol: string
  /** Natural isotopic abundance (%) */
  abundance: number
  /** Uncertainty on abundance (%) or null if not available */
  uncertainty: number | null
}

/**
 * IAEA isotopic abundance data for ${abundances.length} naturally occurring nuclides
 * across ${Object.keys(elementAbundances).length} elements.
 */
export const IAEA_ABUNDANCES: readonly IAEAAbundance[] = ${JSON.stringify(abundances, null, 2)}

/**
 * Get IAEA abundance data for a specific element
 * @param elementSymbol - Element symbol (e.g., 'H', 'Li', 'Ni')
 * @returns Array of IAEAAbundance entries for that element
 */
export function getIAEAAbundancesForElement(elementSymbol: string): IAEAAbundance[] {
  // Handle hydrogen isotopes: D and T map back to H for lookup
  const lookupSymbol = (elementSymbol === 'D' || elementSymbol === 'T') ? 'H' : elementSymbol

  return IAEA_ABUNDANCES.filter((a) => {
    // For hydrogen, match by symbol since we store D/T separately
    if (lookupSymbol === 'H') {
      return a.symbol === elementSymbol
    }
    return a.symbol === lookupSymbol
  })
}

/**
 * Get IAEA abundance for a specific nuclide
 * @param symbol - Element symbol (e.g., 'Li', 'D')
 * @param massNumber - Mass number A
 * @returns Abundance percentage or null if not found
 */
export function getIAEAAbundance(symbol: string, massNumber: number): number | null {
  const entry = IAEA_ABUNDANCES.find((a) => a.symbol === symbol && a.a === massNumber)
  return entry ? entry.abundance : null
}

/** Metadata about this IAEA data extraction */
export const IAEA_METADATA = {
  source: 'IAEA Nuclear Data Services - LiveChart API',
  url: 'https://nds.iaea.org/relnsd/v1/data',
  dataVintage: 'NuBase 2020',
  extractionDate: '${extractionDate}',
  nuclideCount: ${abundances.length},
  elementCount: ${Object.keys(elementAbundances).length},
} as const
`;

  fs.writeFileSync(OUTPUT_FILE, tsContent, 'utf-8');
  console.log(`Written ${OUTPUT_FILE}`);

  // Print summary comparison for key elements
  console.log('\nAbundance summary for key LENR elements:');
  const keyElements = ['H', 'Li', 'B', 'Ni', 'Pd', 'Fe', 'Cu', 'Ti'];
  for (const el of keyElements) {
    const elData = abundances.filter(a => {
      if (el === 'H') return a.z === 1;
      return a.symbol === el;
    });
    if (elData.length > 0) {
      const isotopes = elData.map(d => `${d.symbol}-${d.a}: ${d.abundance}%`).join(', ');
      console.log(`  ${el}: ${isotopes}`);
    }
  }
}

main().catch((err) => {
  console.error('Error:', err.message);
  process.exit(1);
});
